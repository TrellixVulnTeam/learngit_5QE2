# include <iostream>
# include <cmath>
using namespace std;
class Date
{
	int year,month,day;
	public:
		Date(int y, int m, int d)
    {
    	
    	year=y;
    	if(m>0&&m<13)
    	month=m;
    	if(d>0&&d<360)
    	day=d;
    }
    friend int test(const Date &,const Date &b);
} ;
 int  test(const Date &a,const Date &b)
{
	 return abs(b.year-a.year)*360+abs(b.month-a.month)*30+abs(b.day-a.day) ;
}
int main()
{
	Date a(2018,2,6),b(2017,2,3);
	cout<<test(a,b);
	return 0;
}





























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































