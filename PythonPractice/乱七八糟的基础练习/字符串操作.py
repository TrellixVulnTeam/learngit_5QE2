str = "You are one"
print(str.isalnum())#判断所有字符是字母或数字
print(str.isalpha())#判断所有字符是字母
print(str.isdigit())#判断所有字符是数字
print(str.isupper())#判断所有字符是大写
print(str.islower())#判断所有字符是小写
print(str.istitle())#判断所有字符是首字母大写
print(str.isspace())#判断所有字符是空白符

print(str.title()) #把每个单词的首字母大写
print(str.capitalize())#把第一个单词首字母大写